package com.aliyun.gumiho.sql.bvt.translate.oracle2.ppas.version9_6.ddl.trigger.alter;

import com.aliyun.gumiho.sql.enums.DBType;
import com.aliyun.gumiho.sql.util.SQLUtils;
import org.junit.Assert;
import org.junit.Test;

/**
 * @author kongtong.ouyang on 2018/6/15.
 */
public class OracleAlterTriggerTest_0 {

    @Test
    public void test1() {
        String s = "ALTER TRIGGER update_job_history DISABLE; ";

        String formatSQL = SQLUtils.format(s, DBType.Oracle);
        System.out.println(s);
        System.out.println("------------------");
        System.out.println(formatSQL);
        Assert.assertEquals("ALTER TRIGGER update_job_history DISABLE;", formatSQL);
    }


    @Test
    public void test2() {
        String s = "ALTER TRIGGER update_job_history ENABLE; ";


        String formatSQL = SQLUtils.format(s, DBType.Oracle);
        System.out.println(s);
        System.out.println("------------------");
        System.out.println(formatSQL);
        Assert.assertEquals("ALTER TRIGGER update_job_history ENABLE;", formatSQL);
    }

}
